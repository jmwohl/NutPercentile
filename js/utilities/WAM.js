	/*
	*	Returns zscore, text category, and percentile value from LMSArray and numValue.  
	*	Format is determined by the property called returnFormat.
	*
	*	@TODO: Refactor this to return an object of the z-score, percentile, flag 
	*/
	resultStr: function(LMSArray, numValue) {
		var resp = {};
		if (typeof(LMSArray) != "undefined") {
			var L=LMSArray[0]
			var M=LMSArray[1]
			var S=LMSArray[2]
			var ZScore=0;
			var outputStr=String(this.returnFormat);
			var category=0;
			var percentile=0;
			
			if (L != 0) {        
				ZScore =  (Math.pow((numValue/M),L) - 1 ) / (L*S);
			} else {
				//This is the log to the base e function, despite the notation
				ZScore = Math.log(numValue/M)/S;
			}
			resp.zscore = ZScore.toFixed(3);
			
			percentile=this.PCTILEFROMZ(ZScore).toFixed(3);
			resp.percentile = percentile;
			
			outputStr=outputStr.replace(/Z/,ZScore.toFixed(3));
			outputStr=outputStr.replace(/P/, percentile);
			if ((percentile >=0) && (percentile<=5)) {
				category=this.strcat[1];
			} else if (percentile<=85) {
				category=this.strcat[2];
			} else if (percentile<=95) {
				category=this.strcat[3];
			} else if (percentile<=100) {
				category=this.strcat[4];
			} else {
				category=this.strcat[5];
			}
			outputStr=outputStr.replace(/text/i,category);
			resp.flag = category;
			return resp;
			// return outputStr //+ M;  

		} else {
			return resp;
			// return "No reference data available.";
		}
	},
	
	NUTANYAGE: function(MF, AGEMO, KILOS, CM) { 
		if ((MF!="f") && (MF!="F")) { 
			MF="M";
		}
	  	MF=MF.toUpperCase();
	  	if (AGEMO>=0) {
			if (CM>0) {
				//If AGEMO<=24 and cm >0 returns values for infant weight for height
				if (AGEMO<=24) {
					if (KILOS>0) {
						return this.WTLENINF(MF,AGEMO,KILOS,CM);
					} else {
						return this.LENAGEINF(MF,AGEMO,KILOS,CM);
					}  
				} 
				//If AGEMO>24 and AGEMO<=240, returns z score based on BMI for Age up to age 20. 
				if ((AGEMO>24) && (AGEMO<=240)) {
					if (KILOS>0) {
						return this.BMIAGE(MF,AGEMO,KILOS,CM);
					} else {
						return this.HEIGHTAGE(MF,AGEMO,KILOS,CM);
					}
				}
				//If AGEMO>240, returns text as WHO category based on crude BMI
				if (AGEMO>240) { 
					var result="??";
					var BMIValue=this.BMI(KILOS,CM);
					//Adult of more than 20 years
					if ((BMIValue < 18.5) && (BMIValue > 3.2)) {result=this.strcat[1]}; 
					if ((BMIValue >= 18.5) && (BMIValue < 25)) {result=this.strcat[2]}; 
					if ((BMIValue >= 25.0) && (BMIValue < 30)) {result=this.strcat[3]}; 
					if ((BMIValue > 30) && (BMIValue < 55)) {result=this.strcat[4]}; 
					if ((BMIValue <= 3.2) || (BMIValue > 55)) {result=this.strcat[0]}; 
					return this.t("BMI")+"="+BMIValue+ "\: "+ result;
				}  
			} else {
				// No cm is supplied, returns z score based on infant Weight for Age up to 36 months,
				if (AGEMO<=24) {
					return this.WTAGEINF(MF,AGEMO,KILOS);
				} else if (AGEMO <=240) {
					return this.WTAGE(MF,AGEMO,KILOS);
				} else {    //?Are there standards for adults based on weight alone?
					return "??"+this.t("CM")+"??"
				}
	        }
	    } else {
			// Age less than zero, shouldn't happen
			return "??"+this.t("Age")+"??";
	    }
	},
	
	
	/*
	*	Calculate BSA (Body Surface Area)
	*/
	BSA: function(KILOS, CM) {
		return (Math.pow((CM * KILOS)/3600, 0.5)).toFixed(2);
	},
	
	/*
	*	Calculate BMI (Body Mass Index)
	*/
	BMI: function(KILOS, CM) { 
	     var crudeBMI=(KILOS* 10000) / (CM * CM );
	     return crudeBMI.toFixed(2);
	},
	
	/*
	*	Calculate percentile based on z-score
	*/
	PCTILEFROMZ: function(Z) {
		// Algorithm AS66 Applied Statistics (1973) vol22 no.3
		// Computes P(Z<z)
		var upper=false;
		var alnorm=0;
		//When z=1.96, the values returned are .975 (upper=false) or .025(upper=true) (roughly).
		var z=parseFloat(Z); //note upper case Z converted to lower.
		// upper=(form.upper[0].checked ? true:false);
		// upper must be set to true or false when the function is called
		var ltone = 7.0;
	    var utzero = 18.66;
	    var con= 1.28;
	    var a1 = 0.398942280444;
	    var a2 = 0.399903438504;
	    var a3 = 5.75885480458;
	    var a4 = 29.8213557808;
	    var a5 = 2.62433121679;
	    var a6 = 48.6959930692;
	    var a7 = 5.92885724438;
	    var b1 = 0.398942280385;
	    var b2 = 3.8052e-8;
	    var b3 = 1.00000615302;
	    var b4 = 3.98064794e-4;
	    var b5 = 1.986153813664;
	    var b6 = 0.151679116635;
	    var b7 = 5.29330324926;
	    var b8 = 4.8385912808;
	    var b9 = 15.1508972451;
	    var b10 = 0.742380924027;
	    var b11 = 30.789933034;
	    var b12 = 3.99019417011;

		if(z<0) {
			upper=!upper;
			z=-z;
		}
		if(z<=ltone || upper && z<=utzero) {
			var y=0.5*z*z;
			if(z>con) {
				alnorm=b1*Math.exp(-y)/(z-b2+b3/(z+b4+b5/(z-b6+b7/(z+b8-b9/(z+b10+b11/(z+b12))))));
			}
			else {
				alnorm=0.5-z*(a1-a2*y/(y+a3-a4/(y+a5+a6/(y+a7))));
			}
		} else {
			alnorm=0;
	    }
		if(!upper) alnorm=1-alnorm;
		return(alnorm*100);
	}, //End of P from Z function
		
